package pass;


public class AssignmentOperators {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        int c = 3;

        double d = 1.4;
        double e = 2.6;
        double f = 3.7;

        a += 2;
        a -= 1;
        b *= 5;
        c /= 1;
        c %= 5;

        d += 2.6;
        d -= 1.2;
        e *= 5;
        f /= 1;
        f %= 5;
    }
}